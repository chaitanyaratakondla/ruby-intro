# In Ruby, there are different *types* of data:

# Numbers
puts 5

# Perform simple math with numbers
puts 5+2

# Strings
puts "Hello, world!"

# Combine strings together
puts "Tacos are" + "awesome"
puts "Mu" + ("ha"*10) + " I'm a bad man"
puts "Tacos" + 3.to_s

# Variables
y=3
x = 10
puts x*y

food = "tacos"
quantity = 3
puts food*quantity

# Combine strings and variables
first_name = "Boba"
greeting = "This is the Way, #{first_name}"
puts greeting

# String manipulation
puts "Hello".reverse